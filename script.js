var a=1;
function showorhide1(){
    if (a==1){
        document.getElementById("greybox1").style.display="flex";
        document.getElementById("plus_btn_1").style.transform='rotate(45deg)'; 

        return a=0;
    }
    else{
        document.getElementById("greybox1").style.display="none";
        document.getElementById("plus_btn_1").style.transform='rotate(90deg)'; 

        return a=1;   
    }
}
